package com.sgtesting.practise;

public class Continuewhile 
{

	public static void main(String[] args) 
	{
		int i = 0;
	      while (i < 10) 
	            {
	             if (i == 4) 
	                {
	                continue;
	                }
	                System.out.print(i);
	                i++;
	             }


	}

}
